package org.openwes.common.utils.constants;

import lombok.Data;
import lombok.experimental.UtilityClass;

@Data
@UtilityClass
public class MarkConstant {

    /**
     * #号（井号）电话语音提示里，常被称为：pound key
     */
    public static final String POUND_KEY = "#";

    /**
     * *号（米号）电话语音里常读作：star key
     * asterisk也可以
     */
    public static final String STAR_KEY = "*";

    /**
     * 逗号，
     */
    public static final String COMMA_MARK = ",";

    /**
     * ．句号
     */
    public static final String FULL_STOP_MARK = ".";

    /**
     * :冒号
     */
    public static final String COLON_MARK = ":";

    /**
     * ;分号
     */
    public static final String SEMICOLON_MARK = ";";

    /**
     * 空格符号
     */
    public static final String SPACE_MARK = " ";

    /**
     * ！ exclamation mark 惊叹号
     */
    public static final String EXCLAMATION_MARK = "!";

    /**
     * ？ question mark 问号
     */
    public static final String QUESTION_MARK = "?";

    /**
     * - 连字符
     */
    public static final String HYPHEN_CHARACTER = "-";

    /**
     * ^ 插入符号、插入符、脱字符号、脱字符等
     */
    public static final String CARET_CHARACTER = "^";

    /**
     * 下划线
     */
    public static final String UNDERSCORE = "_";

    /**
     * 单引号
     */
    public static final String SINGLE_QUOTATION_MARK = "'";

    /**
     * 双引号
     */
    public static final String DOUBLE_QUOTATION_MARK = "\"";

    /**
     * ‖双线号
     */
    public static final String PARALLEL_MARK = "||";

    /**
     * /斜线号
     */
    public static final String DIAGONAL_MARK = "/";

    /**
     * ＆并且
     */
    public static final String AND_MARK = "&";

    /**
     * |   竖线
     */
    public static final String VERTICAL_BAR = "|";

    /**
     * \   反斜线
     */
    public static final String BACKSLASH = "\\";


}
